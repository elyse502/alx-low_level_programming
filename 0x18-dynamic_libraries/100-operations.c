/**
 * add - Addition.
 * @a: First number.
 * @b: Second number.
 * Return: result.
 */
int add(int a, int b)
{
      return (a + b);
}
/**
 * sub - Substraction.
 * @a: First number.
 * @b: Second number
 * Return: result.
 */
int sub(int a, int b)
{
      return (a - b);
}
/**
 * mul - Multiplication.
 * @a: First number.
 * @b: Second number.
 * Return: result.
 */
int mul(int a, int b)
{
      return (a * b);
}
/**
 * div - Division.
 * @a: First number.
 * @b: Second number.
 * Return: result.
 */
int div(int a, int b)
{
      return (a / b);
}
/**
 * mod - Modulus operartor(Remainder).
 * @a: First number.
 * @b: Second number.
 * Return: result.
 */
int mod(int a, int b)
{
      return (a % b);
}
